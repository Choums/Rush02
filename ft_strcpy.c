char	*ft_strcpy(char *dest, char *src)
{
	int i;
	int j;

	i = 0;
	while (src[i] == '0')
		i++;
	j = 0;
	while (src[i] != '\0')
	{
		dest[j] = src[i];
		i++;
		j++;
	}
	dest[j] = src[i];
	return (dest);
}
