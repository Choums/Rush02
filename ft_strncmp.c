int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	if (n)
	{
		while (s1[i] == s2[i] && s1[i] != '\0' && n)
		{
			i++;
			n--;
			if (s1[i] == '\0' && s2[i] == '\0')
				break ;
		}
		if (n)
		{
			return (s1[i] - s2[i]);
		}
	}
	return (0);
}
